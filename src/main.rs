fn main() -> Result<(), Box<dyn std::error::Error>> {
    if let Some(de) = std::env::var_os("XDG_CURRENT_DESKTOP") {
        if de != "mango" {
            eprintln!("You are not using mangowm please use mangowm so neko-mangowm can work");
            return Err("Not mango".into());
        }
    }
    let args: Vec<String> = std::env::args().collect();
    if args.len() == 2 {
        let conn = Connection::connect_to_env()?;
        let (globals, mut event_queue) = registry_queue_init(&conn)?;
        let qh = event_queue.handle();

        let compositor = CompositorState::bind(&globals, &qh)?;
        let layer_shell = LayerShell::bind(&globals, &qh)?;
        let shm = Shm::bind(&globals, &qh)?;

        let surface = compositor.create_surface(&qh);
        let layer =
            layer_shell.create_layer_surface(&qh, surface, Layer::Overlay, Some("oneko"), None);
        layer.set_anchor(Anchor::TOP | Anchor::LEFT);
        layer.set_size(SIZE, SIZE);
        // Position relative to the full output, ignoring bars' reserved space.
        layer.set_exclusive_zone(-1);
        layer.set_keyboard_interactivity(KeyboardInteractivity::None);

        // Empty input region: clicks pass through the cat.
        let input_region = Region::new(&compositor)?;
        layer
            .wl_surface()
            .set_input_region(Some(input_region.wl_region()));

        layer.commit();

        let pool = SlotPool::new((SIZE * SIZE * 4) as usize, &shm)?;

        let mut cat = Cat {
            registry_state: RegistryState::new(&globals),
            output_state: OutputState::new(&globals, &qh),
            shm,
            pool,
            layer,
            _input_region: input_region,
            configured: false,
            exit: false,
            monitor: Some(args[1].trim().to_string()),
            screen_w: 0.0,
            screen_h: 0.0,
            monitor_pos_x: 0.0,
            monitor_pos_y: 0.0,
            win_x: 0.0,
            win_y: 0.0,
            last_cursor: (0.0, 0.0),
            dir: Dir::E,
            frame: false,
            idle_ticks: 0,
        };

        while !cat.exit {
            if cat.configured {
                cat.tick();
            }
            // Flushes pending requests (margin + buffer commit) and processes events.
            event_queue.roundtrip(&mut cat)?;
            thread::sleep(Duration::from_millis(125));
        }
    } else {
        eprintln!("Please provide a Monitor output")
    }

    Err("Fail".into())
}
